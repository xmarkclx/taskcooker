/**
 * Browser-preview snapshot. When `boomerang-preview-todo-count` is set in
 * localStorage, pads the seed project with generated To Do rows so long-list
 * performance can be reproduced without a Tauri backend.
 */
export function previewSeedSnapshot(): AppSnapshot {
  const count = readPreviewTodoCount();
  if (count === 0) {
    return seedSnapshot;
  }

  return {
    ...seedSnapshot,
    todos: [
      ...seedSnapshot.todos,
      ...Array.from({ length: count }, (_, index) => generatedPreviewTodo(index)),
    ],
  };
}
