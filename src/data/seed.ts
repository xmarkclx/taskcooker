const PREVIEW_TODO_COUNT_KEY = 'boomerang-preview-todo-count';

function readPreviewTodoCount(): number {
  try {
    if (typeof window === 'undefined') {
      return 0;
    }
    const raw = window.localStorage.getItem(PREVIEW_TODO_COUNT_KEY);
    const count = raw === null ? 0 : Number(raw);
    return Number.isInteger(count) && count > 0 ? Math.min(count, 20_000) : 0;
  } catch {
    return 0;
  }
}

